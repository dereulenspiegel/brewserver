package de.akuz.brewcontroller;

public class BrewControllerException extends Exception {

	public BrewControllerException() {
		super();
	}

	public BrewControllerException(String message, Throwable cause) {
		super(message, cause);
	}

	public BrewControllerException(String message) {
		super(message);
	}

	public BrewControllerException(Throwable cause) {
		super(cause);
	}

}
